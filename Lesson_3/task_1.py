# 1. В диапазоне натуральных чисел от 2 до 99 определить,
# сколько из них кратны каждому из чисел в диапазоне от 2 до 9.

numbers = []
for n in range(2, 100):
    for i in range(2, 10):
        if n % i == 0:
            numbers.append(n)

print(f'{len(numbers)} кратны любому из чисел в диапазоне от 2 до 9.')
